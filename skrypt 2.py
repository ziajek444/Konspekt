zmienna01 = 50
zmienna02 = 'A'
zmienna03 = chr(zmienna01)
zmienna04 = ord(zmienna02)
finish = " w tabeli ASCII\n"
print(str(zmienna01)+": "+zmienna03,zmienna02+": "+str(zmienna04),sep=finish,end=finish)

